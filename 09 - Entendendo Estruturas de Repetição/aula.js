/*
for loop

"let" vai ficar alterando, na primeira vez ele vai começar com zero, podendo
alterar o valor de início
"i < array.length;" estrutura condicional que retorna valor booleano
"index++" o que vai acontecer depois que terminar de executar o "for"

"i++" é como:
let i = 1;
i = i + 1;
Ele incrementa 1 na própria variável

*/

for (let i = 0; i < 10; i++) {
    console.log(i);
}

