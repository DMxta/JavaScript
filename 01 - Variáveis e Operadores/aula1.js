//"-" = subtração
let subtracao = 10 - 10;
console.log(subtracao);

//"+" = adição
let adicao = 10 + 10;
console.log(adicao);

//"*" = multiplicação
let multiplicacao = 10 * 10;
console.log(multiplicacao);

//"/" = divisão
let divisao = 10 /10;
console.log(divisao);
