//"let" para valores que podemos alterar futuramente
//"=" = recebe a função de atribuir um valor
let variavel = 10;

//console.log() = para imprimir o valor no console
console.log(variavel);

variavel = 20;

console.log(variavel);

//"const" para valores que não podem sofrer alteração
const valorUnico = 30;

console.log(valorUnico);

